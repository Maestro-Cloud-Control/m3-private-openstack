package io.maestro3.agent.model.network.impl.ip;

import org.hibernate.validator.constraints.NotBlank;


public class AddressPool {
    @NotBlank
    private String start;
    @NotBlank
    private String end;

    public AddressPool() {
    }

    public AddressPool(String start, String end) {
        this.start = start;
        this.end = end;
    }

    public String getStart() {
        return start;
    }

    public void setStart(String start) {
        this.start = start;
    }

    public String getEnd() {
        return end;
    }

    public void setEnd(String end) {
        this.end = end;
    }

    public static AddressPool of(String start, String end) {
        return new AddressPool(start, end);
    }
}
